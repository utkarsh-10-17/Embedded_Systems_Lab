ORG 00H
ARRAY:               
    DB 01H, 02H, 03H, 04H, 05H 
	MOV R0, #ARRAY
	MOV R2, #05H
	MOV A, #00H
	MOV B, #00H
	MOV R1, #00H
	
	UPPER_LOOP:
	MOV A, @R0
	ANL A, #01H
	JZ EVEN
	
	INC B
	SJMP NEXT_ELEMENT
	
	EVEN: 
		INC R1
	
	NEXT_ELEMENT:
		INC R0
		DJNZ R2, UPPER_LOOP

END